no =int(input("Enter an integer between 1 and 7 (inclusive): "))
if 1 <= no <= 7:
        
        if no == 1:
            print("One for sorrow")
        elif no == 2:
            print("Two for joy")
        elif no == 3:
            print("Three for a girl")
        elif no == 4:
            print("Four for a boy")
        elif no == 5:
            print("Five for silver")
        elif no == 6:
            print("Six for gold")
        elif no == 7:
            print("Seven for a secret never to be told")
else:
        print("Not a permitted number")
